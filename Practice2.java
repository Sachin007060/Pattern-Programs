// Outer loop will decide how many rows will be printed example if (i<=7) then in this case 7 rows will be printed..

public class Practice2 {
    public static void main(String[] args) {
        for (int i = 1; i <= 7; i++) {
            for (int j = 1; j <= 7; j++) {
                System.out.print("Row:"+i+" ");
            }
            System.out.println();
        }
    }
}