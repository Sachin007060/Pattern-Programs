// Inner loop decides how many columns will be printed example :- if(j<=7) then 7 columns willl get printed.

public class Practice3 {
    public static void main(String[] args) {
        for (int i = 1; i <= 7; i++) {
            for (int j = 1; j <= 7; j++) {
                System.out.print("Column:"+j+" ");
            }
            System.out.println();
        }
    }
}